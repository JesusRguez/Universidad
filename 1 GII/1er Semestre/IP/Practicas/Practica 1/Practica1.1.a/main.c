#include <stdio.h>
#include <stdlib.h>
int main()
{
printf("El número es %d \n", 15); //%d Indica que su salida es un número entero con signo en base decimal.
printf("El número es %i \n", 15); //%i Indica lo mismo que el %d.
printf("El número es %x \n", 15); //%x Indica que su salida es un número entero con signo en base hexadecimal.
printf("El número es %o \n", 15); //%o Indica que su salida es un número entero con signo en base octal.
printf("El número es %e \n", 15); //%e Indica que su salida es un número real con signo usando la notación e.
printf("El número es %E \n", 15); //%E Indica que su salida es un número real con signo usando la notación E.
return 0;
}
