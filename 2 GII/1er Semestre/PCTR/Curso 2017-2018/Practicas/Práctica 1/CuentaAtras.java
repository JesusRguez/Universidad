/**
* Programa en Java que escribe una cuenta atrás en la pantalla.
*
* @author Jesús Rodríguez Heras
* @version 1.0
*/
public class CuentaAtras
{
    public static void main (String[] args)
    {
        int a = 5;
        
        while(a>=0)
        {
            System.out.println(a);
            a--;
        }
    }
}

